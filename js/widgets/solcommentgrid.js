$.widget("ck.commentgrid", {
   //#################################################################
   //# Definition
   //#################################################################
   options: {
      mission: null,
   },

   //#################################################################
   //# Constructor
   //#################################################################
   _create: function () {
      var oGrid = new cSolGridRenderer(
         this.options.mission.ID,
         this.element,
         "comments.php",
         "solcomment.php",
      )
      oGrid.show_sol_grid({ o: "topsolindex" })
   },
})
